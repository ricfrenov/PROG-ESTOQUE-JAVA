    private void OkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkActionPerformed
        //pegar do campo de texto
        
        String Nome = Entrada_Nome_Material.getText();
        String Unidade = Entrada_unidade.getText();
        String Valor = Entrada_de_Valor.getText();
        String CodigoBarras = Entrada_Codigo_Barras.getText();
        

        if ((Entrada_Nome_Material.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
        } else {
// instanciando a classe MaterialInsert do pacote Insert e criando seu objeto Insert
            MaterialInsert Insert = new MaterialInsert();
            Insert.adiciona(Nome, Unidade, CodG, Valor, CodigoBarras, CodF);
            
        }
// apaga os dados preenchidos nos campos de texto

        Entrada_Nome_Material.setText("");
        Entrada_unidade.setText("");
        Entrada_Codigo_Barras.setText("");
        Entrada_de_Valor.setText("");


    }//GEN-LAST:event_OkActionPerformed


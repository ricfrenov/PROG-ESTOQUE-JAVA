    public Incluir_Materiais() {
        initComponents();
        try{
            
            ResultSet rsG = stmt.executeQuery("SELECT CODIGO_GRUPO,NOME_GRUPO FROM GRUPOS");
            while(rsG.next()){
                Lista_Nome_Grupo.addItem(rsG.getString("NOME_GRUPO"));
                CodG = (rsG.getInt("CODIGO_GRUPO"));
            }
                
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"ERRO"+ex);
        }
        try{
            ResultSet rsF = stmt.executeQuery("SELECT CODIGO_FORNECEDOR,NOME_FORNECEDOR FROM FORNECEDOR");
            while(rsF.next()){
                Lista_Nome_Fornecedor.addItem(rsF.getString("NOME_FORNECEDOR"));
                CodF = (rsF.getInt("CODIGO_FORNECEDOR"));
            }
        }catch(SQLException ez){
            JOptionPane.showMessageDialog(null,"Erro"+ez);
            
        }
    }
